'use client';

import { useState } from 'react';

interface ParseResult {
  url: string;
  title: string;
  description: string;
  content: string;
  metadata: {
    ogTitle?: string;
    ogDescription?: string;
    ogImage?: string;
    ogType?: string;
    canonical?: string;
    author?: string;
    publishedTime?: string;
    keywords?: string[];
  };
  structuredData: object[];
  links: { text: string; href: string }[];
  headings: { level: number; text: string }[];
  parseTime: number;
  contentLength: number;
  cached: boolean;
}

